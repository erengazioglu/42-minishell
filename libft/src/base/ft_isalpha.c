#include "../../include/libft_base.h"

/**
 * @brief Checks if a character represents an alphabetical value.
 * @param c		Character to check.
 */
bool	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	return (0);
}
